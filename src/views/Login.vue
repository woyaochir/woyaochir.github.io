<template>
  <el-form ref="form" label-width="70px" :inline="true" class="login-container" :model="form" :rules="rules">
    <h3 class="login-title">系统登录</h3>
    <el-form-item label="用户名" prop="username"> <!-- 编写表单域部分 -->
      <el-input v-model="form.username" placeholder="请输入账号"></el-input>
    </el-form-item>
    <el-form-item label="密码" prop="password">
      <el-input type="password" v-model="form.password" placeholder="请输入密码"></el-input>
    </el-form-item>
    <el-form-item>
      <el-button @click="submit" style="margin-left:105px" type="primary">登录</el-button>
    </el-form-item>
  </el-form>
</template>
<script>
// import Mock from 'mockjs'
import Cookie from 'js-cookie' // set设置 remove删除 get获取cookie的某个值
import _jsCookie301JsCookie from 'js-cookie'
import {getMenu} from '../api'
  export default {
    data() {
      return {
        form: {
          username: '',
          password: ''
        },
        rules: {
          username: [
            {required:true, trigger:'blur'/* 表示失去焦点时触发 */, message:'请输入用户名'}
          ],
          password:[
            {required:true, trigger:'blur'/* 表示失去焦点时触发 */, message:'请输入用密码'}
          ]
        }
      }
    },
    methods: {
      // 登录
      submit() {
        // 返回token
        // const token = Mock.Random.guid() // 调用后会生成随机数
        
        // Cookie.set('token', token)
        

        // form表单的校验通过
        // 调用这个函数会传入两个参数 这是其中一个 如果成功valid == true  // 当这个参数存在 代表校验通过
        this.$refs.form.validate((valid) => {
          if(valid) {
            getMenu(this.form).then(({ data }) => {
              if(data.code === 20000) {
                console.log(data);
                // 将token信息存入cookie 用于不同页面之间的通信
                Cookie.set('token', data.data.token)

                // 获取菜单的数据，存入store
                // data.data.menu
                this.$store.commit('setMenu', data.data.menu)
                this.$store.commit('addMenu', this.$router)
                console.log(this.$router);
                // 跳转至首页
                this.$router.push('/home')
              } else {
                this.$message.error(data.data.message);
              }
            }) 
          }
        })

        // this.$router.push('/home')
      }
    }
  }
</script>
